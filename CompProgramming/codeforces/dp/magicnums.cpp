constexpr int md = int(1e9) + 7;
using Mint = Modular<std::integral_constant<decay<decltype(md)>::type, md>>;
Mint dp[MX][10][MX];

Mint calc(int n1, string a){
    memset(dp, 0, sizeof(dp));
    dp[0][1][0] = 1;
    for (int l = 0; l < n1; l++){
        for (int k = 0; k < 2; k++){
            for (int md = 0; md < m; md++){
                for (int di = 0; di <= (k ? a[l] - '0' : 9); di++){ //1 means it's odd

                    if (!l && !di) continue;
                    if (l % 2 == 1 && di != d) continue;
                    if (l % 2 == 0 && di == d) continue;

                    dp[l + 1][k && di == a[l] - '0'][(10*md + di) % m] += dp[l][k][md]; //handled by bc

                }
            }
        }
    }
    Mint res = 0;
    res += dp[n1][0][0];
    res += dp[n1][1][0];

    return res;
}

int valid(string s, int n){
    int res = 0;
    for (int i = 0; i < n; i++){
        if (i % 2 == 0 && s[i] - '0' == d) return 0;
        if (i % 2 && s[i] - '0' != d) return 0;
        res = (10*res + (s[i] - '0')) % m;
    }
    return res == 0;
    
}
